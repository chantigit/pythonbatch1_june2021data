#Function1 Preparation  (Sub Program1)
def   add(a,b):
       c=a+b
       print(c)
#Function2 Preparation   (Sub Program2)
def   sub(a,b):
       c=a-b
       print(c)
# Function3 Preparation  (Sub Program3)
def mul(a,b):
    c=a*b
    print(c)
#Function1 usage
add(10,20)
add(100,200)
add(1000,2000)
#Function2 usage
sub(10,2)
sub(100,20)
#Function3 usage
mul(10,20)

#1.No code duplication (Have code reusability)
#2.Less Space & Time complexicity
#3.Have code readability
#4.Extensibility is becomes easy
#5.Modularity(Dividing main program into sub program) is achieving




